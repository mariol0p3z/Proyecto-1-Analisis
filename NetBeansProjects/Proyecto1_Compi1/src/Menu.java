    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("*.olc", "olc");
        jFileChooser1.setAcceptAllFileFilterUsed(false);
        jFileChooser1.addChoosableFileFilter(filtro);
        
        int accion = jFileChooser1.showOpenDialog(null);
        
        if (accion != jFileChooser1.APPROVE_OPTION){
            return;
        }
        else{
            try{
                BufferedReader reader = new BufferedReader(new FileReader(jFileChooser1.getSelectedFile()));
                jTextArea1.read(reader, null);
            } catch (IOException ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

