    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("*.olc", "olc");
        jFileChooser1.setAcceptAllFileFilterUsed(false);
        jFileChooser1.addChoosableFileFilter(filtro);
        
        int accion = jFileChooser1.showSaveDialog(null);
        if(accion != jFileChooser1.APPROVE_OPTION){
            return;
        }
        else{
            String fileName = jFileChooser1.getSelectedFile().getAbsolutePath().toString();
            if (!fileName.contains(".olc")){
                fileName = fileName + " .olc";
                try{
                    BufferedWriter writer = new BufferedWriter(new FileWriter(fileName));
                    jTextArea1.write(writer);
                } catch(IOException ex){
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jMenuItem4ActionPerformed

